//C1-M1

function openModalC1M1() {
    document.querySelector('.overlay-c1-m1').style.display = 'block';
    document.querySelector('.modal-c1-m1').classList.add('modal--open');
}

function closeModalC1M1() {
    document.querySelector('.overlay-c1-m1').style.display = 'none';
    document.querySelector('.modal-c1-m1').classList.remove('modal--open');
}


