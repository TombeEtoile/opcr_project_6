//C3-M1

function openModalC3M1() {
    document.querySelector('.overlay-c3-m1').style.display = 'block';
    document.querySelector('.modal-c3-m1').classList.add('modal--open');
}

function closeModalC3M1() {
    document.querySelector('.overlay-c3-m1').style.display = 'none';
    document.querySelector('.modal-c3-m1').classList.remove('modal--open');
}


