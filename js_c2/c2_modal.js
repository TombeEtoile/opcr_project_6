//C2-M1

function openModalC2M1() {
    document.querySelector('.overlay-c2-m1').style.display = 'block';
    document.querySelector('.modal-c2-m1').classList.add('modal--open');
}

function closeModalC2M1() {
    document.querySelector('.overlay-c2-m1').style.display = 'none';
    document.querySelector('.modal-c2-m1').classList.remove('modal--open');
}


